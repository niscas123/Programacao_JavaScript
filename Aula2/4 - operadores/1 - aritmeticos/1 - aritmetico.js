// Operador binário. Retorna o inteiro restante de divisões do operando

12 % 5 // Retorna 2

// Incremento (++) e Decremento (--)
++x
x++

const a = ++2; // 3
const b = 2++; // 2

--y
y--

const c = --2 // 1
const d = 2-- // 1

// Negação (-) e Adição (+)
-3
+"3" // Retorna 3
+true // Retorna 1
+false // Retorna 0
-true // Retorna -1

// Operador de exponenciação (**)
2 ** 3 // Retorna 8
10 ** -1 // Retorna 0.1

// Operador de agrupamento ()
2 * (2 + 5)
